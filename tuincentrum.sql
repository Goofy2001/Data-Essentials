CREATE TABLE PLANTEN
(
    Art_code    NUMBER(3),
    Plantennaam VARCHAR2(20) NOT NULL,
    Soort       VARCHAR2(10) NOT NULL,
    Kleur       VARCHAR2(10),
    Hoogte      NUMBER(4),
    Bloeibegin  NUMBER(2),
    Bloeieinde  NUMBER(2),
    Prijs       NUMBER(4, 2) NOT NULL,
    CONSTRAINT pk_planten_art_code PRIMARY KEY (Art_code)
);

CREATE TABLE LEVERANCIERS
(
    Lev_code   NUMBER(3),
    Lev_naam   VARCHAR2(20) NOT NULL,
    Adres      VARCHAR2(25) NOT NULL,
    Woonplaats VARCHAR2(15) NOT NULL,
    Korting    NUMBER(2, 1) NOT NULL,
    CONSTRAINT pk_leveranciers_lev_code PRIMARY KEY (Lev_code)
);

CREATE TABLE OFFERTES
(
    Lev_code  NUMBER(3),
    Art_code  NUMBER(3),
    Lev_tijd  NUMBER(2),
    Off_prijs NUMBER(4, 2) NOT NULL,
    CONSTRAINT pk_offertes_lev_code_art_code PRIMARY KEY (Lev_code, Art_code),
    CONSTRAINT fk_offertes_lev_code_leveranciers FOREIGN KEY (Lev_code) REFERENCES LEVERANCIERS,
    CONSTRAINT fk_offertes_art_code_planten FOREIGN KEY (Art_code) REFERENCES PLANTEN
);

CREATE TABLE BESTELLINGEN
(
    Bestelnr    NUMBER(4),
    Lev_code    NUMBER(3) NOT NULL,
    Besteldatum DATE      NOT NULL,
    Lev_datum   DATE,
    Bedrag      NUMBER(6) NOT NULL,
    CONSTRAINT pk_bestellingen_bestelnummer PRIMARY KEY (Bestelnr),
    CONSTRAINT fk_bestellingen_lev_code_leveranciers FOREIGN KEY (Lev_code) REFERENCES LEVERANCIERS
);

CREATE TABLE BESTELREGELS
(
    Bestelnr    NUMBER(4),
    Art_code    NUMBER(3),
    Aantal      NUMBER(4)    NOT NULL,
    Bestelprijs NUMBER(4, 2) NOT NULL,
    CONSTRAINT pk_bestelregels_bestelnr_art_code PRIMARY KEY (Bestelnr, Art_code),
    CONSTRAINT fk_bestelregels_bestelnr_bestellingen FOREIGN KEY (Bestelnr) REFERENCES BESTELLINGEN,
    CONSTRAINT fk_bestelregels_art_code_planten FOREIGN KEY (Art_code) REFERENCES PLANTEN
);

CREATE TABLE PLANTSCHEMA
(
    Categorie VARCHAR2(4),
    Hoogte1   NUMBER(4) NOT NULL,
    Hoogte2   NUMBER(4) NOT NULL,
    Afstand   NUMBER(3) NOT NULL,
    CONSTRAINT pk_plantschema_categorie PRIMARY KEY (Categorie)
);

